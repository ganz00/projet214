package simulation;

public class Saison {
	private int Numero;
	private String Nom;
	private int Debut;
	private int Fin;
	
	public int getNumero() {
		return Numero;
	}
	public void setNumero(int numero) {
		Numero = numero;
	}
	public int getDebut() {
		return Debut;
	}
	public void setDebut(int debut) {
		Debut = debut;
	}
	public int getFin() {
		return Fin;
	}
	public void setFin(int fin) {
		Fin = fin;
	}
	public String getNom() {
		return Nom;
	}
	public void setNom(String nom) {
		Nom = nom;
	}
	
	
	

}
